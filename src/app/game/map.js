class MapCell {
	constructor(x, y, width, height, type) {
		this._x = x;
		this._y = y;
		this._type = type;
		this._unit = null;

		this._screenX = this._x * width;
		this._screenY = this._y * width;

		this._polygon = [];
		this._polygon.push({ x: this._screenX, y: this._screenY });
		this._polygon.push({ x: this._screenX + width, y: this._screenY });
		this._polygon.push({ x: this._screenX + width, y: this._screenY + height });
		this._polygon.push({ x: this._screenX, y: this._screenY + height });
	}

	get x() {
		return this._x;
	}

	get y() {
		return this._y;
	}

	get type() {
		return this._type;
	}

	set unit(value) {
		return this._unit = value;
	}

	get unit() {
		return this._unit;
	}

	get polygon() {
		return this._polygon;
	}
}

class Map {

	constructor() {
		this._cells = [];

		for (let y = 0; y < map.length; y++) {
			this._cells.push([]);
			for (let x = 0; x < map[0].length; x++) {
				this._cells[y].push(new MapCell(x, y, constants.tileWidth, constants.tileWidth, map[y][x]));
			}
		}

		this._height = map.length;
		this._width = map[0].length;
	}

	getCell(x, y) {
		return this._cells[y][x];
	}

	get width() {
		return this._width;
	}

	get height() {
		return this._height;
	}

	draw(context) {
		const tileWidth = constants.tileWidth;

		for (let y = 0; y < map.length; y++) {
			for (let x = 0; x < map[y].length; x++) {
				context.beginPath();
				context.rect(x * tileWidth, y * tileWidth, tileWidth, tileWidth);
				if (map[y][x] == 1) {
					context.fillStyle = 'gray';
				}
				if (map[y][x] == 0) {
					context.fillStyle = 'white';
				}
				if (map[y][x] == 2) {
					context.fillStyle = 'green';
				}
				context.fill();
				context.stroke();
			}
		}
	}
}

